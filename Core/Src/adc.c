        case ADC_FEEDBACK_2_CHANNEL_INDEX_PROBING_3V3:
            return FEEDBACK_ANALOG_INDEX_PROBING_3V3;
        case ADC_FEEDBACK_2_CHANNEL_INDEX_AIRP_OPEN_MEC:
